function [T_K, P_Pa, rho_kgm3] = atmosphereISA(altitude_m, tempDev_C)
% ISA standard atmosphere with temperature deviation
% Based on International Standard Atmosphere (ISA)

% ISA constants
T0 = 288.15;              % Sea level temperature [K] (15°C)
P0 = 101325;              % Sea level pressure [Pa]
g = 9.80665;              % Gravity [m/s²]
R = 287.05;               % Gas constant [J/(kg·K)]
L = -0.0065;              % Lapse rate [K/m] in troposphere

% Standard temperature at altitude
T_std_K = T0 + L * altitude_m;

% Add temperature deviation
T_K = T_std_K + tempDev_C;

% Pressure (using standard lapse rate)
if altitude_m < 11000  % Troposphere
    P_Pa = P0 * (T_std_K / T0)^(-g / (L * R));
else  % Stratosphere (simplified)
    P_11000 = P0 * (216.65 / T0)^(-g / (L * R));
    P_Pa = P_11000 * exp(-g * (altitude_m - 11000) / (R * 216.65));
end

% Density
rho_kgm3 = P_Pa / (R * T_K);
end

function densityAlt_m = calculateDensityAltitude(pressureAlt_m, tempDev_C)
% Calculate density altitude from pressure altitude and temperature deviation
% Density altitude = pressure altitude + 120 ft per °C above standard

densityAlt_ft = pressureAlt_m / 0.3048;  % Convert to feet
densityAlt_ft = densityAlt_ft + (120 * tempDev_C);  % Add temperature effect
densityAlt_m = densityAlt_ft * 0.3048;  % Convert back to meters
end

