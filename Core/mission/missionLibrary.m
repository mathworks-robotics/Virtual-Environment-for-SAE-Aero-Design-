        mission.Environment.AirDensity_kgm3, mission.Environment.AirDensity_kgm3/1.225*100);

fprintf('\n--- Wind Conditions (Aerospace Blockset) ---\n');
fprintf('Wind Speed at 6m: %.2f m/s from %d° (%.1f mph)\n', ...
        mission.Environment.WindShear.WindSpeed_6m_mps, ...
        mission.Environment.WindShear.WindDirection_6m_deg, ...
        mission.Environment.WindShear.WindSpeed_6m_mps*2.237);
