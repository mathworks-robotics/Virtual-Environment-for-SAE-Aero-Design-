function mission = missionLibrary(missionNumber)
% MISSIONLIBRARY Pre-defined SAE Aero Design mission scenarios
%
% Returns complete mission configuration for specified mission number
% Includes Aerospace Blockset wind model parameters for Simulink
%
% SYNTAX:
%   mission = missionLibrary(missionNumber)
%
% INPUTS:
%   missionNumber - Integer 1-10 (see list below)
%
% OUTPUTS:
%   mission - Complete mission structure with all parameters
%
% AVAILABLE MISSIONS:
%   === CORE COMPETITION MISSIONS (Required) ===
%   1 - Baseline Flight           (1.0x) Easy
%   2 - Warm Day Operations       (1.5x) Moderate  
%   3 - Gusty Conditions          (2.0x) Moderate-Hard
%   4 - High Altitude Challenge   (2.5x) Hard
%   5 - Adverse Weather           (3.0x) Very Hard
%
%   === OPTIONAL CHALLENGE MISSIONS ===
%   6 - Precision Landing         (1.8x) Moderate
%   7 - Maximum Payload           (2.2x) Hard
%   8 - Speed Run                 (1.5x) Moderate
%   9 - Endurance Challenge       (2.0x) Moderate-Hard
%   10 - Emergency Procedures     (3.5x) Extreme
%
% EXAMPLE:
%   % Load Mission 1 (Baseline)
%   mission = missionLibrary(1);
%   
%   % Load Mission 5 (Adverse Weather)
%   mission = missionLibrary(5);
%
% Author: MathWorks Aerospace Team
% Date: 2026-01-13
% Updated: 2026-01-13 - Added Aerospace Blockset wind parameters

%% Validate Input
if nargin < 1
    error('Mission number required. Use: mission = missionLibrary(1) for Mission 1');
end

if ~isnumeric(missionNumber) || missionNumber < 1 || missionNumber > 10
    error('Mission number must be 1-10. See "help missionLibrary" for list');
end

%% Load Aircraft Parameters (must exist in workspace)
if ~evalin('base', 'exist(''mass'', ''var'')')
    error('Aircraft parameters not loaded. Run initialSetup1.m first');
end

mass = evalin('base', 'mass');

%% Mission Definitions
switch missionNumber
    case 1
        mission = mission1_Baseline(mass);
    case 2
        mission = mission2_WarmDay(mass);
    case 3
        mission = mission3_Gusty(mass);
    case 4
        mission = mission4_HighAltitude(mass);
    case 5
        mission = mission5_AdverseWeather(mass);
    case 6
        mission = mission6_PrecisionLanding(mass);
    case 7
        mission = mission7_MaxPayload(mass);
    case 8
        mission = mission8_SpeedRun(mass);
    case 9
        mission = mission9_Endurance(mass);
    case 10
        mission = mission10_Emergency(mass);
end

%% Display Mission Summary
displayMissionSummary(mission);

end

